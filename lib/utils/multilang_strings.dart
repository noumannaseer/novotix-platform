class MultiLang {
  static Languages currentLanguage;

  static initLocalLang(BuildContext context) {
    Locale locale = Localizations.localeOf(context);
    print("Local Language: ${locale.languageCode}");

    Values.apiLang =
        locale.languageCode == Values.en || locale.languageCode == Values.nl
            ? locale.languageCode
            : Values.en;
    currentLanguage = getLang(locale);
  }

  static Languages getLang(Locale locale) {
    switch (locale.languageCode) {
      case Values.en:
        return LanguageEng();
      case Values.nl:
        return LanguageDutch();
      default:
        return LanguageEng();
    }
  }
}
